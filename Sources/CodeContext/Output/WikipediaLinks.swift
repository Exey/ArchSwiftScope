// Exey Panteleev
// Maps DSMatch/AlgoMatch/DetectedDesignPattern display names to their
// Wikipedia article. Slugs are sourced from the hyperlinks actually used on
// the three canonical index pages (https://en.wikipedia.org/wiki/List_of_data_structures,
// https://en.wikipedia.org/wiki/List_of_algorithms, and
// https://en.wikipedia.org/wiki/Software_design_pattern) rather than guessed,
// so casing/punctuation matches the real article title. A handful of our
// display names don't appear on any index (e.g. Apple's DispatchQueue, or
// niche structures too new/obscure for the list); those best-effort entries
// are marked inline. Names with no confident target are simply absent — the
// report falls back to plain (unlinked) text for those.
import Foundation

enum WikipediaLinks {

    static func url(forDataStructure name: String) -> String? {
        dsSlug[name].map(articleURL)
    }

    static func url(forAlgorithm name: String) -> String? {
        algoSlug[name].map(articleURL)
    }

    static func url(forPattern name: String) -> String? {
        patternSlug[name].map(articleURL)
    }

    private static func articleURL(_ slug: String) -> String {
        var allowed = CharacterSet.alphanumerics
        allowed.insert(charactersIn: "-_.~()',")
        let encoded = slug.addingPercentEncoding(withAllowedCharacters: allowed) ?? slug
        return "https://en.wikipedia.org/wiki/\(encoded)"
    }

